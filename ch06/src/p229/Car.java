package p229;

public class Car {
	//필드
	public int speed;
	
	//생성자
	
	//메소드
	public int getSpeed() {
		return speed;
	}
	
	public void keyTurnOn() {
		System.out.println("키를 돌립니다.");
	}
	
	public void run() {
		for(int i=0; i<=50; i+=10) {
			speed = i;
			System.out.println("달립니다.(시속:"+ speed + "km/h)");
		}
	}
}
